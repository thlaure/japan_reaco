<div class="text-justify mt-5">
    <p class="lead">The goal is to make the visit of Hiroshima and get an overview of main monuments and points of interests. This will help to teach users the history of the city and to learn in a fun way.</p>
    <p class="lead">The application is available on any Android smartphone.</p>
