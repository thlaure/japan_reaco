<div class="text-justify mt-5">
    <p class="lead">Hiroshimap is a smartphone application using geolocalization that permits gathering points of interest on the Saigoku road. The application offers another option which consists in using the smartphone camera to exploit AR (Augmented Reality) technology. The application uses this AR technology to display essential information thanks to a virtual character.</p>
</div>
