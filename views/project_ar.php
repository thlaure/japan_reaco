<h3 class="display-4 text-center">Hiroshimap</h3>
<div class="text-justify mt-5">
    <p class="lead">Due to the Coronavirus crisis, tourism has been really impacted in the entire world, including the city of Hiroshima. Everyone's freedom is limited, and priorities go more towards evenings with friends and family than towards culture and the discovery of heritage.</p>
    <p class="lead">That’s why we have developed the “Hiroshimap” smartphone application to help revitalize tourism in Hiroshima.</p>
</div>
